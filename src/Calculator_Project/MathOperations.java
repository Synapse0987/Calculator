public class MathOperations{
    public float addition(float x, float y){
        return(x+y);
    }
    public float minus(float x, float y){
        return(x-y);
    }
    public float division(float x, float y){
        return(x/y);
    }
    public float multiply(float x, float y){
        return(x*y);
    }
}